let contador = 1
// '++' incremento

//incrementa 1, então o contador passa a valer 2
contador++
//incrementa 1 novamente, então o contador passa a valer 3 agora
contador++

console.log(contador)

// '--' decremento

//decrementa 1, então o contador passa a valer 2, lembrando que isso é após os incrementos acima
contador--
//decrementa 1 novamente, então o contador passa a valer 1 agora
contador--

console.log(contador)