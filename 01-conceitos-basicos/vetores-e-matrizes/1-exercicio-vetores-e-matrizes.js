//alocando strings em vetor
let pokemon = ["Pikachu", "Charmander", "Bulbassauro"]

//metodo que decrementa o vetor (diminui uma posição, automaticamente "eliminando" o ultimo elemento do vetor)
pokemon.pop()

//output do vetor todo
console.log(pokemon)

//metodo que eliminado a primeira posicao do vetor
pokemon.shift()

//output = "Charmander"
console.log(pokemon)